package HashSets;

import java.util.HashSet;

public class HashSets
{
	public static void main(String[] args){

		HashSet<String> names = new HashSet<String>();//One datatype
		names.add("James");
		names.add("Smith");
		names.add("John");
		names.add("Mary");
		names.add("Anda");

		System.out.println("Names: " + names);
	}
}
